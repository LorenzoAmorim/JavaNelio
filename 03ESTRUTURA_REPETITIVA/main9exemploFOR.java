public class main9exemploFOR {
    public static void main(String[] args) {
        int y=10;//y=10
        for(int i=0; i<4; i++){//i=0
            System.out.print(i);//i=0
            y=y+i;//y=10 E i=0, CADA VEZ O i AUMENTANDO 1
            System.out.println(y);//y=10 e aumentando 1
        }
    }
}
